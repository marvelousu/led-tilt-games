// Phase 3 (1500-2500ms): 文字中心から放射バースト + 中間でチャイム
// Phase 4 (2500-3000ms): ランダム順に画素消灯 + SE_HIT
// ※16 行版は文字を縦 2 倍に拡大し、粒子・バーストを増強する
// ------------------------------------------------------------

void boot_init();
void boot_update();          // 毎フレーム呼ぶ (内部で描画)
bool boot_is_finished();
