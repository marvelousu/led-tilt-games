#pragma once

// ============================================================
//   config.h : コンパイル時設定
// ============================================================
// MATRIX_EXT:
//   有効化 (define) すると 16×32 版 (FC16 2枚) でビルド
//   未定義のままなら 8×32 版 (FC16 1枚)
// ------------------------------------------------------------
#define MATRIX_EXT

#ifdef MATRIX_EXT
  #define DISP_ROWS       16
  #define MAX_DEVICES     8
  #define USE_JAPANESE_UI 1
#else
  #define DISP_ROWS       8
  #define MAX_DEVICES     4
  #define USE_JAPANESE_UI 0
#endif

#define DISP_COLS         32
#define PLAY_COLS         31  // col 31 は鬼ごっこのタイマーゲージ専用

// ---- タイミング ----
#define TARGET_FPS        30
#define FRAME_MS          33

// ---- ハードウェア ----
#define LED_INTENSITY     3   // MAX7219 輝度 (0-15)
#define CS_PIN            10  // MAX7219 CS
#define BUZZER_PIN        9

// ============================================================
//   ジャイロ操作 調整パラメータ (LSM6DSV16X / Qwiic = Wire1)
// ============================================================
//   ジャイロ操作系の閾値はすべてこの区画に集約。実機で調整する
//   ときはここの数値だけを変えればよい。考え方は
//   docs/gyro_tuning.md を参照。
//   ・I2C アドレス 0x6B、生レジスタ読み出し (gyro.cpp)
//   ・加速度は mg 単位 (1g = 1000mg)
// ------------------------------------------------------------

// [傾き] 方向入力が成立する傾きの大きさ (mg)
//   下げる→敏感(わずかな傾きで反応) / 上げる→鈍感   目安 200-500
#define IMU_TILT_THRESHOLD_MG     300

// [シェイク] シェイク成立とみなす加速度の大きさ (mg, XYZ 合成)
//   下げる→弱い振りで反応 / 上げる→強く振らないと反応   目安 1500-2800
#define IMU_SHAKE_THRESHOLD_MG   2000

// [シェイク] チャタリング防止: 一度成立した後、加速度が SHAKE
//   閾値のこの割合(%)まで下がると解除され、再検出できるようになる
//   下げる→連続シェイクしにくい / 上げる→しやすい   目安 60-85
#define IMU_SHAKE_RELEASE_PCT      75

// [縦持ち回転] 1サンプルがこの値以下なら静止ノイズとして無視 (ジャイロ raw LSB)
//   下げる→微小な揺れも拾う / 上げる→ゆっくりの回転を取りこぼす   目安 150-600
#define GYRO_ROT_DEADZONE         300

// [縦持ち回転] 回転量の積算がこの値に達したら「90°回した」と判定
//   下げる→少し回しただけで成立 / 上げる→しっかり回さないと成立しない
//   実測で 90°回転 ≒ 6万〜15万 (ジャイロFS=gyro.cpp の CTRL6_VAL に依存)
//   目安 40000-150000
#define GYRO_ROT_THRESHOLD        60000
